static std::atomic<int>  g_threads{0};
static std::atomic<int>  g_vis{0};
static std::atomic<bool> g_first_vi{false};
// Highest stage3 state-machine value (D_800CE6AC) observed during the run. Lets the boot
// smoke test assert the init cascade reaches the title state machine rather than just
// "didn't crash". See state_probe(). (0 = never read; the cold-boot seed is state 1.)
static std::atomic<int>  g_max_state{0};
static std::atomic<int>  g_swaps{0};

// VI budget before a clean quit. PERMANENT harness knob (not throwaway diagnostic debt):
// LAMBO_MODERN_MAX_VIS is consumed by the pivot boot smoke test (tests/pivot/
// test_boot_smoke.py) and used to probe the gfx seam (#58). Defaults split by mode since
// the RT64 default flip (2026-07-02): a headless run (LAMBO_HEADLESS=1) is a boot probe
// and keeps the ~2 s / 120-VI cap; a presenting run is PLAY MODE and gets no cap (close
// the window / kill the process to quit).
static int quit_after_vis() {
    if (const char* e = std::getenv("LAMBO_MODERN_MAX_VIS")) {
        int v = std::atoi(e);
        if (v > 0) return v;
    }
    return lambo_rt64::enabled() ? std::numeric_limits<int>::max() : 120;
}
static const int kQuitAfterVis = quit_after_vis();

// Stall watchdog timeout. MUST exceed the wall-time the VI budget needs, or a raised
// LAMBO_MODERN_MAX_VIS is silently truncated by the watchdog before reaching the cap.
// Derive it from the cap (headless runs well above 10 VI/s, so cap/10 is generous) with
// a 30 s floor for the default boot. Uncapped play mode gets no watchdog (INT_MAX/10
// seconds is effectively never, and killing a live game window from a stall heuristic
// would be wrong anyway).
static int watchdog_seconds() {
    return std::max(30, kQuitAfterVis / 10 + 5);
}

