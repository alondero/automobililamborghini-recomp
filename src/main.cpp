// Print the one-line boot summary the runtime guard (tests/pivot/test_boot_smoke.py) parses,
// then terminate the process immediately. We _Exit instead of returning through
// recomp::start()'s teardown on purpose: that teardown joins the helper threads and munmaps
// RDRAM, but it does NOT terminate the 7 game osCreateThread threads -- once the main game
// thread actually runs its per-frame dispatch loop, those threads are live in native
// osRecvMesg and race the munmap into a SIGSEGV. This is a headless boot/probe harness that is
// quitting anyway, so skip the unwind and let process exit tear the game threads down.
// (Graceful game-thread shutdown is RT64-integration work, #58.)
[[noreturn]] static void boot_summary_and_exit() {
    std::fprintf(stderr, "[probe] boot summary; threads=%d vis=%d first_vi=%d max_state=%d swaps=%d\n",
                 g_threads.load(), g_vis.load(), (int)g_first_vi.load(), g_max_state.load(),
                 g_swaps.load());
    std::fflush(nullptr);
    std::_Exit(g_first_vi.load() ? 0 : 2);
}

// Set by headless::create_render_context (stub_renderer.cpp) so this retrace hook can reach RDRAM.
extern uint8_t* g_lambo_rdram;

// Emulate libultra's retrace-time __osViSwapContext for THIS game's osScheduler (#58).
//
// ultramodern reimplements the PUBLIC libultra VI API (osViSwapBuffer/osViGetCurrentFramebuffer)
// as natives, but this game's osScheduler reaches into the PRIVATE VI-manager state __osViCurr
// (0x8008D1A0) / __osViNext (0x8008D1A4) that only libultra's VI INTERRUPT used to maintain. Its
// gfx-task scheduling gate (__scTaskReady -> func_80074380) runs a task only when
//   __osViCurr->buffer (+0x4) == __osViNext->buffer (+0x4).
// On real hardware the VI retrace ISR's __osViSwapContext promotes __osViCurr <- __osViNext every
// frame, so once a requested swap is applied the two match again and the gate reopens. ultramodern
// replaced that ISR (and __osViInit is a no-op stub), so __osViCurr stayed frozen at 0 while
// __osViNext advanced to the pending framebuffer -> the gate latched shut after frame 1 and only the
// first gfx OSTask ever reached the renderer (the #58 1-task stall). Mirroring the buffer field each
// retrace reopens the gate with correct 1-frame-per-retrace pacing. This is faithful libultra VI
// behaviour, not a scaffold, so it is the default. (When RT64 lands this hook stays — it is the
// game's registered retrace callback.)
static void promote_vi_context() {
    uint8_t* rdram = g_lambo_rdram;
    if (rdram == nullptr) return;
    auto gw = [&](uint32_t a) -> uint32_t { return *(uint32_t*)(rdram + (a - 0x80000000u)); };
    auto sw = [&](uint32_t a, uint32_t v) { *(uint32_t*)(rdram + (a - 0x80000000u)) = v; };
    auto in_rdram = [](uint32_t a) { return a >= 0x80000000u && a < 0x80800000u; };
    uint32_t curr = gw(0x8008D1A0); // __osViCurr
    uint32_t next = gw(0x8008D1A4); // __osViNext
    if (!in_rdram(curr) || !in_rdram(next)) return;
    // Promote the WHOLE 0x30 context, not just the buffer field: ares RDRAM dumps show the two
    // OSViContexts byte-identical every frame (title dump 0x8008D140/0x8008D170), which is the
    // hardware __osViSwapContext's net effect. The buffer word (+0x4) is the scheduler gate;
    // modep (+0x8) and control (+0xC) are live since osViSetMode (func_80075C60) was un-stubbed
    // (W115: LPN2 0x8008C4A0 at boot, LAN2 0x8008C540 from the game SM -- matches ares).
    for (uint32_t off = 0; off < 0x30; off += 4) sw(curr + off, gw(next + off));
    // VI present-path bridge (#58, default since the 2026-07-02 RT64 flip): apply the game's
    // VI-manager state to ultramodern's VI natives at retrace cadence -- the exact values and
    // timing libultra's __osViSwapContext would have used. osViSetMode only on a modep
    // transition (the native reconfigures the display; hardware re-applies registers every
    // frame but the config is idempotent). Unconditional (also headless): ultramodern's VI
    // state simply goes unconsumed without a presenting renderer, and one code path keeps the
    // trunk stress-tested. NOTE events.cpp patch-0002's dummy-mode hunk is NOT retired with the
    // flip: W115 observed dummy-mode scanout live, i.e. VI ticks do land in the game-started ->
    // first-osViSetMode window while RT64/Vulkan init delays the first tick -- without the
    // hunk's mode==nullptr guard, update_vi() derefs null there. It is a null-guard for that
    // race, not a scaffold.
    {
        uint32_t modep = gw(next + 0x8);
        uint32_t buf   = gw(next + 0x4);
        static uint32_t s_last_modep = 0;
        if (in_rdram(modep) && modep != s_last_modep) {
            osViSetMode(rdram, (int32_t)modep);
            s_last_modep = modep;
        }
        if (in_rdram(buf))   osViSwapBuffer(rdram, (int32_t)buf);
    }
}

// Stage3 state-machine progression tracker (PERMANENT harness instrumentation, not a
// throwaway diagnostic). The ROM's per-frame dispatcher (func_800028D0) walks a state
// register at D_800CE6AC and advances copyright(2)->...->title(6)->demo-race(8). We sample
// it each VI, record the max reached (g_max_state, asserted by the boot smoke test) and log
// each transition, so a run reports "stuck vs progressing" instead of just "didn't crash".
//
// Addressing (measured 2026-06-28, ares-cross-checked): the register lives at its LITERAL
// address 0x800CE6AC (NOT the -0xC00 boot shift -- that applies to IPL3-loaded code, not this
// BSS global; 0x800CDAAC stays 0). ultramodern stores RDRAM so a native u32 load yields the
// N64 word, but a halfword needs the MEM_H XOR-2 swizzle -- hence the (a&2) extract below,
// which matches recomp.h's MEM_H exactly. The progression is 0->1->2->3->4->5->6 then (as of
// 2026-06-28) STUCK at 6 (ares walks 0..6->7->8). The 6->7 advance lives in func_80038D6C
// (state-6 worker, guest 0x8003816C): the hold counter 0x800985AC expires and the fade
// D_800A2D08 reaches 0 -- the SAME gate values ares has at state 7 -- yet a hardware watchpoint
// confirms `state = 7` is never written. Mechanism not yet isolated (the recomp body is
// auto-generated computed-goto C; -O2 + multithread make gdb line-tracing unreliable). #53.
static void state_probe() {
    uint8_t* rdram = g_lambo_rdram;
    if (rdram == nullptr) return;
    auto h = [&](uint32_t a) -> uint32_t { // N64 big-endian halfword at literal addr (MEM_H)
        uint32_t w = *(uint32_t*)(rdram + ((a & ~3u) - 0x80000000u));
        return (a & 2u) ? (w & 0xFFFF) : ((w >> 16) & 0xFFFF);
    };
    int state = (int)h(0x800CE6AC);
    if (state > g_max_state.load()) g_max_state.store(state);
    static int s_last = -1;
    if (state != s_last) {
        std::fprintf(stderr, "[state] vi=%d  state=%d (was %d)\n", g_vis.load(), state, s_last);
        s_last = state;
    }
}

// Menu-screen probe (PERMANENT harness instrumentation, same class as state_probe): samples the
// menu overlay's current-screen id D_80098562 (set by the "request menu screen" API func_800383A8,
// docs/notes/menu.md) plus its neighbour D_80098560 each VI and logs transitions -- the menu chain
// (players/mode/series/car/name/pak-message, #69) all runs under top-level state 6, so state_probe
// alone cannot show menu navigation.
static void menu_probe() {
    uint8_t* rdram = g_lambo_rdram;
    if (rdram == nullptr) return;
    auto h = [&](uint32_t a) -> uint32_t { // N64 big-endian halfword at literal addr (MEM_H)
        uint32_t w = *(uint32_t*)(rdram + ((a & ~3u) - 0x80000000u));
        return (a & 2u) ? (w & 0xFFFF) : ((w >> 16) & 0xFFFF);
    };
    int scr = (int)(int16_t)h(0x80098562);
    int sub = (int)(int16_t)h(0x80098560);
    static int s_scr = -9999, s_sub = -9999;
    if (scr != s_scr || sub != s_sub) {
        std::fprintf(stderr, "[menu] vi=%d  screen=%d sub=%d (was %d/%d)\n",
                     g_vis.load(), scr, sub, s_scr, s_sub);
        s_scr = scr; s_sub = sub;
    }
}

// Frame-pace probe (PERMANENT harness instrumentation, same class as state_probe): counts game
// framebuffer swaps by sampling __osViNext->buffer (ctx at *(0x8008D1A4), buffer field +0x4) once
// per VI. The scheduler gate admits at most one swap per retrace, so per-VI sampling cannot miss
// one. Real hardware runs this game at ~30fps = 0.5 swaps/VI (ares state-8 dwell 3094 VIs, W102);
// ~1.0 swaps/VI means the port is running the game 2x fast (#58 pacing).
static void pace_probe(int vi_n) {
    uint8_t* rdram = g_lambo_rdram;
    if (rdram == nullptr) return;
    auto gw = [&](uint32_t a) -> uint32_t { return *(uint32_t*)(rdram + (a - 0x80000000u)); };
    uint32_t next = gw(0x8008D1A4); // __osViNext
    if (next < 0x80000000u || next >= 0x80800000u) return;
    uint32_t buf = gw(next + 0x4);
    static uint32_t s_last_buf = 0;
    if (buf != s_last_buf) { s_last_buf = buf; g_swaps.fetch_add(1); }
    if ((vi_n % 600) == 0) {
        static int s_prev_total = 0;
        int total = g_swaps.load();
        std::fprintf(stderr, "[pace] vi=%d swaps_last_600vi=%d (~%.1f fps)\n",
                     vi_n, total - s_prev_total, (total - s_prev_total) / 10.0);
        s_prev_total = total;
    }
}

extern "C" void lambo_thread_trace_dump(int vi); // THROWAWAY diag (mesgqueue.cpp), gated by LAMBO_THREAD_TRACE
static void vi_cb() {
    promote_vi_context();
    state_probe();
    menu_probe();
    int n = ++g_vis;
    pace_probe(n);
    if ((n % 50) == 0) lambo_thread_trace_dump(n); // THROWAWAY: per-thread recv/send snapshot to find the frozen thread
    if (!g_first_vi.exchange(true)) std::fprintf(stderr, "[probe] FIRST VI retrace\n");
    if (n == kQuitAfterVis) {
        std::fprintf(stderr, "[probe] reached %d VIs; quitting\n", n);
        boot_summary_and_exit();
    }
}

// Audio RSP ucode (M_AUDTASK = task type 2, aspMain). librecomp ships no audio ucode and a real
// recompiled aspMain is a large standalone effort -- the faithful end state, tracked under the AUDIO
// epic #53 (this no-op is the scaffold to retire when real aspMain lands). #58 is the descent-crash
// epic this boundary belongs to.
// SCAFFOLD: this no-op ucode signals RspExitReason::Broke so run_task() succeeds (rsp.cpp:55) and the
// audio thread proceeds. It synthesises NO PCM, so it does NOT fill (or zero) the audio DMA buffer:
// osAiSetNextBuffer (routed to the native, gen_syms_toml.py) queues whatever stale RDRAM sits in that
// buffer into ultramodern's audio backend. That is inaudible TODAY only because the headless build has
// no audio sink; once a real output device is driven, audio_ucode_noop must be replaced by real aspMain
// (or made to zero the buffer) to avoid emitting garbage -- it is NOT a silence guarantee. The point of
// the scaffold is purely to clear the CRASH boundary: the native route runs the submit path WITHOUT the
// raw AI-MMIO poll that used to SIGSEGV in __osAiDeviceBusy (func_80080950). Same scaffold class as the
// no-op __osViInit/osCreatePiManager. Gfx tasks (type 1) never reach here -- intercepted by the renderer.
static RspExitReason audio_ucode_noop(uint8_t* /*rdram*/, uint32_t /*ucode_addr*/) {
    static std::atomic<bool> logged{false};
    if (!logged.exchange(true))
        std::fprintf(stderr, "[probe] audio_ucode_noop: first M_AUDTASK (type 2) -> Broke (no PCM; #53)\n");
    return RspExitReason::Broke;
}

// RSPRecomp'd audio microcode (W135, 2026-07-04, #53): generated from the ROM's aspMain text at
// ROM 0x88B90 (see recomp/aspMain.us.toml for the ground-truth derivation + regen command). This
// RETIRES the audio_ucode_noop scaffold above for the M_AUDTASK path: librecomp's run_task loads
// the OSTask + ucode data into DMEM and this function synthesises real PCM into the game's audio
// buffers, which osAiSetNextBuffer then queues into the SDL sink.
RspExitReason aspMain(uint8_t* rdram, uint32_t ucode_addr);

static RspUcodeFunc* get_rsp_microcode_stub(const OSTask* task) {
    if (task->t.type == 2) return aspMain; // M_AUDTASK: RSPRecomp'd real synthesis (#53)
    static std::atomic<bool> logged{false};
    if (!logged.exchange(true))
        std::fprintf(stderr, "[probe] get_rsp_microcode: unhandled task type %u -> nullptr\n",
                     (unsigned)task->t.type);
    return nullptr;
}

