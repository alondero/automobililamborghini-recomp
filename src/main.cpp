// Real SDL2 keyboard + gamepad -> N64 OSContPad, feeding ultramodern's get_input callback
// (which packs our button mask + float stick into OSContPad via osContGetReadData).
//
// Threading: SDL events pump on the MAIN thread (update_gfx_stub); get_input/poll_input are
// called on the GAME thread (osContStartReadData). So the main thread SAMPLES SDL state into
// one atomic snapshot each frame (input_sample()), and the game thread only READS the
// snapshot -- no cross-thread SDL calls, no lock. Snapshot layout (uint32_t):
//   [15:0] = N64 button mask, [23:16] = stick_x (int8), [31:24] = stick_y (int8).
//
// Port 0 is reported connected UNCONDITIONALLY (input_device_info below) even with no physical
// pad: the keyboard is a valid input device, and the ROM's object-slot gate (func_8007A8A0)
// bails to a stuck state 6 unless port 0 reads as present (#64/#53).
//
// LAMBO_MODERN_INPUT (env, hex OS_CONT mask) is OR'd into every read -- the permanent harness
// knob for headless title-advance scripting (LAMBO_HEADLESS never inits SDL gamecontroller, so
// its snapshot stays 0 and get_input returns exactly the env mask, as before this change).

// N64 OSContPad button bits (libultra ultra64/controller.h; standard across all N64 titles).
enum {
    N64_A       = 0x8000, N64_B      = 0x4000, N64_Z     = 0x2000, N64_START = 0x1000,
    N64_DU      = 0x0800, N64_DD     = 0x0400, N64_DL    = 0x0200, N64_DR    = 0x0100,
    N64_L       = 0x0020, N64_R      = 0x0010,
    N64_CU      = 0x0008, N64_CD     = 0x0004, N64_CL    = 0x0002, N64_CR    = 0x0001,
};

// N64 hardware stick maxes at roughly +-80 after calibration; games are tuned for that range,
// so we scale full deflection to +-80 (faithfulness) rather than the full int8 +-127.
static constexpr int   N64_STICK_MAX   = 80;
static constexpr int   PAD_AXIS_DEADZONE = 8000;   // ~24% of int16 range (SDL recommended ~8000)
static constexpr int   PAD_TRIG_THRESH   = 8000;   // analog trigger -> digital
static constexpr int   PAD_CSTICK_THRESH = 12000;  // right-stick -> C-buttons

static uint16_t g_held_buttons = 0;                 // LAMBO_MODERN_INPUT env override, OR'd in
static int8_t   g_held_sx = 0, g_held_sy = 0;       // LAMBO_MODERN_INPUT stick override (harness)
// LAMBO_INPUT_PULSE=BTNHEX:PERIOD:DUTY[:STARTVI] -- periodic button pulse for headless menu
// navigation (a held LAMBO_MODERN_INPUT mask is one EDGE forever, so it can advance at most one
// menu; a pulse presses/releases every PERIOD VIs for DUTY VIs, walking a whole menu chain).
static uint16_t g_pulse_buttons = 0;
static int      g_pulse_period = 0, g_pulse_duty = 0, g_pulse_start = 0;
static std::atomic<uint32_t> g_input_snapshot{0};   // main-thread sampled, game-thread read
static SDL_GameController* g_pad = nullptr;          // first opened controller (port 0)

// --- rumble-pak sink (#69) ----------------------------------------------------------------
// The game's SI/PIF bridge (func_8007F780 -> lambo_joybus_answer, recomp/src/libultra_stubs.c)
// runs on the GAME thread and decodes the ROM's motor-control pak writes (joybus cmd 0x03 to
// pak block 0xC000: payload 0x01 = motor on, 0x00 = off). SDL rumble must be driven from the
// MAIN thread (that owns g_pad open/close), so -- exactly like input -- the game thread only
// PUBLISHES an atomic request and the main-thread pump (update_gfx_stub) applies it. Declared
// extern "C" so the C responder in libultra_stubs.c can call lambo_pak_set_rumble().
static std::atomic<int> g_rumble_on{0};              // game-thread write, main-thread read
extern "C" void lambo_pak_set_rumble(int on) { g_rumble_on.store(on ? 1 : 0, std::memory_order_relaxed); }

// Apply the published rumble state to the physical pad. Called once per main-thread pump.
// N64 rumble is bang-bang (motor fully on or off), so map to SDL full strength. We refresh the
// effect every pump with a short expiry (150 ms > one 30 fps frame) so a sustained motor-on
// keeps buzzing without needing a re-trigger, and a single motor-off (or a dropped controller)
// lets it lapse. Idempotent-ish: we only issue an SDL call on an on/off EDGE plus periodic
// refresh while on, to avoid hammering the HID layer every frame.
static void rumble_apply() {
    static int last = 0;
    int on = g_rumble_on.load(std::memory_order_relaxed);
    if (g_pad == nullptr || !SDL_GameControllerGetAttached(g_pad)) { last = 0; return; }
    if (on) {
        SDL_GameControllerRumble(g_pad, 0xFFFF, 0xFFFF, 150);   // refresh; expiry > frame time
    } else if (last) {
        SDL_GameControllerRumble(g_pad, 0, 0, 0);               // off edge: stop immediately
    }
    last = on;
}

static int8_t pad_axis_to_n64(int v) {              // int16 SDL axis -> int8 N64 stick (deadzoned)
    if (v > -PAD_AXIS_DEADZONE && v < PAD_AXIS_DEADZONE) return 0;
    float f = v / 32767.0f;
    if (f >  1.0f) f =  1.0f;
    if (f < -1.0f) f = -1.0f;
    return (int8_t)(f * N64_STICK_MAX);
}

// Sample SDL keyboard + gamepad on the MAIN thread and publish the atomic snapshot.
static void input_sample() {
    uint16_t b = 0;
    int sx = 0, sy = 0;

    if (g_pad != nullptr && SDL_GameControllerGetAttached(g_pad)) {
        auto down = [](SDL_GameControllerButton g) { return SDL_GameControllerGetButton(g_pad, g) != 0; };
        // Mapping is tuned to THIS game's VERIFIED menu control scheme (not the generic
        // Zelda64Recomp default): the ROM's menu driver (func_800030F8) confirms on A|START
        // (andi 0x9000), cancels on B (0x4000), and moves the cursor with the ANALOG STICK only
        // -- it tests no C-button or D-pad bit for menu nav. So the face buttons follow Xbox-native
        // intent: A=confirm, B=cancel. The C-buttons live on X/Y + the right stick, where this
        // game's menus never look at them, so they can't produce phantom menu input.
        // NOTE: the *in-race* meaning of each N64 bit (throttle/brake/camera) is NOT yet ROM-verified
        // -- the race-overlay input consumer hasn't been read. This is a physical Xbox->N64 binding,
        // not a claim about race behaviour; revisit when state 8 becomes playable (see race.md).
        if (down(SDL_CONTROLLER_BUTTON_A))             b |= N64_A;      // confirm (menu)
        if (down(SDL_CONTROLLER_BUTTON_B))             b |= N64_B;      // cancel (menu)
        if (down(SDL_CONTROLLER_BUTTON_X))             b |= N64_CL;     // West  -> C-left
        if (down(SDL_CONTROLLER_BUTTON_Y))             b |= N64_CU;     // North -> C-up
        if (down(SDL_CONTROLLER_BUTTON_START))         b |= N64_START;  // advance / pause
        if (down(SDL_CONTROLLER_BUTTON_LEFTSHOULDER))  b |= N64_L;
        if (down(SDL_CONTROLLER_BUTTON_RIGHTSHOULDER)) b |= N64_R;
        if (down(SDL_CONTROLLER_BUTTON_DPAD_UP))        b |= N64_DU;
        if (down(SDL_CONTROLLER_BUTTON_DPAD_DOWN))      b |= N64_DD;
        if (down(SDL_CONTROLLER_BUTTON_DPAD_LEFT))      b |= N64_DL;
        if (down(SDL_CONTROLLER_BUTTON_DPAD_RIGHT))     b |= N64_DR;
        // N64 Z on the left analog trigger (LT); N64 R also on the right trigger (RT) for racing feel.
        if (SDL_GameControllerGetAxis(g_pad, SDL_CONTROLLER_AXIS_TRIGGERLEFT)  > PAD_TRIG_THRESH) b |= N64_Z;
        if (SDL_GameControllerGetAxis(g_pad, SDL_CONTROLLER_AXIS_TRIGGERRIGHT) > PAD_TRIG_THRESH) b |= N64_R;
        // Remaining C directions: right-stick click -> C-down; the right stick itself -> all four C's.
        if (down(SDL_CONTROLLER_BUTTON_RIGHTSTICK))     b |= N64_CD;    // right-stick click
        int rx = SDL_GameControllerGetAxis(g_pad, SDL_CONTROLLER_AXIS_RIGHTX);
        int ry = SDL_GameControllerGetAxis(g_pad, SDL_CONTROLLER_AXIS_RIGHTY);
        if (rx >  PAD_CSTICK_THRESH) b |= N64_CR;
        if (rx < -PAD_CSTICK_THRESH) b |= N64_CL;
        if (ry >  PAD_CSTICK_THRESH) b |= N64_CD;
        if (ry < -PAD_CSTICK_THRESH) b |= N64_CU;
        // Left analog stick -> N64 stick (SDL Y is +down, N64 stick_y is +up -> negate).
        sx =  pad_axis_to_n64(SDL_GameControllerGetAxis(g_pad, SDL_CONTROLLER_AXIS_LEFTX));
        sy = -pad_axis_to_n64(SDL_GameControllerGetAxis(g_pad, SDL_CONTROLLER_AXIS_LEFTY));
    }

    // Keyboard fallback (always sampled; OR'd with the pad). Arrow keys steer; racing essentials
    // on the left hand. Digital keys drive the stick to full deflection only if the pad is idle.
    if (const Uint8* ks = SDL_GetKeyboardState(nullptr)) {
        if (ks[SDL_SCANCODE_X])      b |= N64_A;       // accelerate / confirm
        if (ks[SDL_SCANCODE_C])      b |= N64_B;       // brake / cancel
        if (ks[SDL_SCANCODE_Z])      b |= N64_Z;
        if (ks[SDL_SCANCODE_RETURN]) b |= N64_START;
        if (ks[SDL_SCANCODE_Q])      b |= N64_L;
        if (ks[SDL_SCANCODE_E])      b |= N64_R;
        if (ks[SDL_SCANCODE_I])      b |= N64_CU;
        if (ks[SDL_SCANCODE_K])      b |= N64_CD;
        if (ks[SDL_SCANCODE_J])      b |= N64_CL;
        if (ks[SDL_SCANCODE_L])      b |= N64_CR;
        int kx = 0, ky = 0;
        if (ks[SDL_SCANCODE_LEFT]  || ks[SDL_SCANCODE_A]) kx -= N64_STICK_MAX;
        if (ks[SDL_SCANCODE_RIGHT] || ks[SDL_SCANCODE_D]) kx += N64_STICK_MAX;
        if (ks[SDL_SCANCODE_UP]    || ks[SDL_SCANCODE_W]) ky += N64_STICK_MAX;
        if (ks[SDL_SCANCODE_DOWN]  || ks[SDL_SCANCODE_S]) ky -= N64_STICK_MAX;
        if (sx == 0 && kx != 0) sx = kx;               // pad stick wins if deflected
        if (sy == 0 && ky != 0) sy = ky;
    }

    uint32_t snap = (uint16_t)b
                  | ((uint32_t)(uint8_t)(int8_t)sx << 16)
                  | ((uint32_t)(uint8_t)(int8_t)sy << 24);
    g_input_snapshot.store(snap, std::memory_order_relaxed);
}

// Open the first connected game controller into port 0 (idempotent; called at init + on hotplug).
static void input_open_controller(int joystick_index) {
    if (g_pad != nullptr) return;
    if (!SDL_IsGameController(joystick_index)) return;
    g_pad = SDL_GameControllerOpen(joystick_index);
    if (g_pad != nullptr)
        std::fprintf(stderr, "[input] controller connected: %s\n",
                     SDL_GameControllerName(g_pad) ? SDL_GameControllerName(g_pad) : "(unknown)");
}
static void input_close_controller(SDL_JoystickID which) {
    if (g_pad == nullptr) return;
    SDL_Joystick* js = SDL_GameControllerGetJoystick(g_pad);
    if (js != nullptr && SDL_JoystickInstanceID(js) == which) {
        SDL_GameControllerClose(g_pad);
        g_pad = nullptr;
        std::fprintf(stderr, "[input] controller disconnected\n");
    }
}

static void input_poll_stub() {}
static bool input_get_input(int controller_num, uint16_t* buttons, float* x, float* y) {
    if (controller_num != 0) return false;
    uint32_t snap = g_input_snapshot.load(std::memory_order_relaxed);
    uint16_t b  = (uint16_t)(snap & 0xFFFF) | g_held_buttons;   // env mask always OR'd (harness knob)
    if (g_pulse_period > 0) {                                   // scripted pulse (harness knob)
        int vi = g_vis.load(std::memory_order_relaxed);
        if (vi >= g_pulse_start && ((vi - g_pulse_start) % g_pulse_period) < g_pulse_duty)
            b |= g_pulse_buttons;
    }
    int8_t   sx = (int8_t)((snap >> 16) & 0xFF);
    int8_t   sy = (int8_t)((snap >> 24) & 0xFF);
    if (sx == 0) sx = g_held_sx;                                // env stick fills in when live stick idle
    if (sy == 0) sy = g_held_sy;
    if (buttons) *buttons = b;
    // ultramodern does stick_x = (int8_t)(127 * x), so divide by 127 (NOT N64_STICK_MAX) to
    // preserve our authentic +-80 range through that re-scale instead of re-expanding to +-127.
    if (x) *x = sx / 127.0f;
    if (y) *y = sy / 127.0f;
    return true;
}
static ultramodern::input::connected_device_info_t input_device_info(int controller_num) {
    using namespace ultramodern::input;
    if (controller_num == 0) return { Device::Controller, Pak::None };
    return { Device::None, Pak::None };
}

int main(int argc, char** argv) {
    // Deterministic lighting self-test: no ROM, no runtime -- exercises the swrender
    // light-decode + lambert path on a synthetic DL and exits (tests/pivot/test_lighting.py).
    if (std::getenv("LAMBO_LIGHTING_SELFTEST")) {
        return headless::run_lighting_selftest();
    }
    const char* rom_path = (argc > 1) ? argv[1] : "Automobili Lamborghini (USA).z64";
    std::fprintf(stderr, "[probe] ROM: %s\n", rom_path);

    register_overlays();

    // Load + apply graphics.json BEFORE recomp::start: ultramodern latches the config
    // and the RT64 context constructor reads it via get_graphics_config(). This is the
    // whole user-facing options mechanism (edit the file; F11 toggles fullscreen live).
    lambo::config::load_and_apply_graphics();

    std::filesystem::path config_dir = std::filesystem::temp_directory_path() / "lambo_modern_probe";
    std::filesystem::create_directories(config_dir);
    recomp::register_config_path(config_dir);

    recomp::GameEntry game{};
    game.rom_hash          = 0x525201d7279f34e3ULL; // XXH3_64(big-endian .z64, padded to /4)
    game.internal_name     = "LAMBORGHINI         "; // ROM header name @0x20 (20 bytes)
    game.game_id           = u8"lamborghini.us";
    game.is_enabled        = true;
    game.entrypoint_address = (gpr)(int32_t)0x80000400u;
    game.entrypoint        = recomp_entrypoint;
    game.on_init_callback  = on_init_cb;
    game.thread_create_callback = thread_create_cb;
    recomp::register_game(game);

    std::u8string game_id = u8"lamborghini.us";
    recomp::RomValidationError verr = recomp::select_rom(rom_path, game_id);
    if (verr != recomp::RomValidationError::Good) {
        std::fprintf(stderr, "[probe] select_rom FAILED (RomValidationError=%d)\n", (int)verr);
        return 1;
    }
    std::fprintf(stderr, "[probe] ROM validated; rom_hash matches\n");

    // recomp::start() blocks the calling thread until ultramodern::quit(), so
    // kick the game off from a side thread once the runtime has set up.
    std::thread starter([game_id]() {
        std::this_thread::sleep_for(std::chrono::milliseconds(150));
        std::fprintf(stderr, "[probe] calling start_game\n");
        std::u8string gid = game_id;
        recomp::start_game(gid);
    });
    starter.detach();

    // Watchdog: guarantee the probe terminates and reports, even on a stall.
    const int wd_sec = watchdog_seconds();
    std::thread watchdog([wd_sec]() {
        std::this_thread::sleep_for(std::chrono::seconds(wd_sec));
        std::fprintf(stderr, "[probe] WATCHDOG %ds: threads=%d vis=%d first_vi=%d\n",
                     wd_sec, g_threads.load(), g_vis.load(), (int)g_first_vi.load());
        // Exit deterministically (same as the VI-cap path) rather than ultramodern::quit(),
        // whose teardown munmaps RDRAM out from under the live game threads (SIGSEGV race).
        boot_summary_and_exit();
    });
    watchdog.detach();

    recomp::Configuration cfg{};
    cfg.project_version = recomp::Version{1, 0, 0, std::string{}};
    cfg.rsp_callbacks.get_rsp_microcode = get_rsp_microcode_stub;
    cfg.renderer_callbacks.create_render_context = headless::create_render_context;
    cfg.gfx_callbacks.create_window = create_window_stub; // avoids start()'s no-window assert
    cfg.gfx_callbacks.update_gfx = update_gfx_stub;       // SDL event pump when RT64 is active
    cfg.events_callbacks.vi_callback = vi_cb;
    cfg.error_handling_callbacks.message_box = message_box_stub;
    // window_handle left default-empty -> create_window_stub() is used.

    // Controller 0 is wired by DEFAULT (#64/#53, 2026-06-29): the ROM's controller subsystem
    // gates object-slot registration on a clean controller status, and ares boots with 4 detected
    // controllers (count D_8011C681=4). With NO controller wired the read returns "no response"
    // and the object-slot gate func_8007A8A0 (via func_80083100) bails -> stuck at state 6.
    // LAMBO_MODERN_INPUT still overrides the held-button mask for input testing (e.g. =1000 holds
    // START); default (unset) = controller 0 connected, no buttons held.
    if (const char* in = std::getenv("LAMBO_MODERN_INPUT")) {
        // Format: BTNHEX[:SX[:SY]]. OS_CONT bits: START=0x1000, A=0x8000, B=0x4000; SX/SY are
        // signed decimal stick values. e.g. LAMBO_MODERN_INPUT=1000 holds START;
        // LAMBO_MODERN_INPUT=0:53:0 holds the stick at x=+53 with no buttons.
        char* end = nullptr;
        g_held_buttons = (uint16_t)std::strtoul(in, &end, 16);
        if (end != nullptr && *end == ':') {
            g_held_sx = (int8_t)std::strtol(end + 1, &end, 10);
            if (end != nullptr && *end == ':') {
                g_held_sy = (int8_t)std::strtol(end + 1, nullptr, 10);
            }
        }
    }
    if (const char* pu = std::getenv("LAMBO_INPUT_PULSE")) {
        // BTNHEX:PERIOD:DUTY[:STARTVI], VI units. e.g. 1000:150:4:300 taps START for 4 VIs
        // every 150 VIs starting at VI 300 -- enough edges to walk the whole menu chain headless.
        char* end = nullptr;
        g_pulse_buttons = (uint16_t)std::strtoul(pu, &end, 16);
        if (end && *end == ':') g_pulse_period = (int)std::strtol(end + 1, &end, 10);
        if (end && *end == ':') g_pulse_duty   = (int)std::strtol(end + 1, &end, 10);
        if (end && *end == ':') g_pulse_start  = (int)std::strtol(end + 1, nullptr, 10);
        std::fprintf(stderr, "[probe] input pulse: btn=%04x period=%d duty=%d start=%d\n",
                     g_pulse_buttons, g_pulse_period, g_pulse_duty, g_pulse_start);
    }
    cfg.input_callbacks.poll_input = input_poll_stub;
    cfg.input_callbacks.get_input = input_get_input;
    cfg.input_callbacks.get_connected_device_info = input_device_info;
    std::fprintf(stderr, "[probe] input: controller0 connected (default), buttons=%04x\n", g_held_buttons);

    // Audio epic #53 (PR 1 of 3): populate the host audio sink. ultramodern
    // reads cfg.audio_callbacks inside recomp::start (recomp.cpp:743), before
    // preinit (recomp.cpp:809) calls init_audio() -> set_audio_frequency(48000).
    // Opening the SDL device now means the runtime's first set_frequency call
    // hits a live queue; the per-game osAiSetNextBuffer_recomp calls later
    // (recomp/funcs_8.c lines 2186, 2828, 3755, 3764, 4075) route the AI buffer
    // into SDL via ultramodern's shim.
    lambo::audio::init(48000);
    lambo::audio::get_callbacks(&cfg.audio_callbacks);
    std::fprintf(stderr, "[probe] audio: callbacks wired (queue_samples/get_frames_remaining/set_frequency)\n");

    std::fprintf(stderr, "[probe] calling recomp::start\n");
    recomp::start(cfg);
    // Reached only via the watchdog quit path (a boot stall before the VI cap): the VI-cap
    // path exits from vi_cb via boot_summary_and_exit() and never returns here.
    boot_summary_and_exit();
}
